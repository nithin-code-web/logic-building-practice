# Problem:
# Check whether a given number is positive, negative, or zero

# Input:
# An integer number

# Output:
# Print "Positive" if number > 0
# Print "Negative" if number < 0
# Print "Zero" if number == 0

# Logic:
num = int(input("enter a number :"))

if num > 0:
    print("positive")
elif num < 0:
    print("negative")
else:
    print("zero")



