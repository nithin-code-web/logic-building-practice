# Problem:
# Check if a number is divisible by 5

# Input:
# An integer number

# Output:
# Print "Divisible by 5" if number is perfectly divisible by 5
# Print "Not divisible by 5" otherwise

# Logic:
num = int(input("enter a number :"))

if num % 5 == 0:
    print('{num} is divisible by 5')
else:
    print('{num} is not divisible by 5') 
    


